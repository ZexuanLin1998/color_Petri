function countDictValueInDictArray(ajson){
  countDict = {}
  for(item in ajson) {
    // 这里要与json的键名对上
    thekey = ajson[item].sour
    if (thekey in countDict){
      countDict[thekey] += 1
    } else{
      countDict[thekey] = 1
    }
    thekey = ajson[item].end
    if (thekey in countDict){
      countDict[thekey] += 1
    } else{
      countDict[thekey] = 1
    }
  }
  return countDict
}
